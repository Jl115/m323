const addition = (num1, num2) => {
  return num1 + num2;
}
const subtraction = (num1, num2) => {
  return num1 - num2;

}
const multiply = (num1, num2) => {
  return num1 * num2;

}
const division = (num1, num2) => {
  return num1 / num2;
}

module.exports = {
  addition,
  subtraction,
  multiply,
  division
}